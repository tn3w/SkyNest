class ProofOfWorkClient {
    constructor(workerCount = navigator.hardwareConcurrency || 4) {
        this.workerCount = workerCount;
        this.workers = [];
        this.currentSolutions = new Map();
    }

    startWorkers(salt, difficulty, form) {
        if (this.currentSolutions.has(salt)) {
            console.warn(`Task with string "${salt}" is already being solved.`);
            return;
        }

        const prefix = "0".repeat(difficulty);
        let solutionFound = false;

        this.workers = Array.from({ length: this.workerCount }, (_, index) => {
            const worker = new Worker(
                URL.createObjectURL(
                    new Blob([
                        `
                const calculateHash = async (input) => {
                    const buffer = new TextEncoder().encode(input);
                    const hashBuffer = await crypto.subtle.digest('SHA-256', buffer);
                    return Array.from(new Uint8Array(hashBuffer))
                        .map((byte) => byte.toString(16).padStart(2, '0'))
                        .join('');
                };

                self.onmessage = async function(e) {
                    const { salt, prefix, start, step, batchSize } = e.data;
                    let nonce = start;

                    while (true) {
                        for (let i = 0; i < batchSize; i++) {
                            const hash = await calculateHash(salt + nonce);
                            if (hash.startsWith(prefix)) {
                                self.postMessage(nonce);
                                return;
                            }
                            nonce += step;
                        }
                    }
                };
                `,
                    ])
                )
            );

            worker.onmessage = (e) => {
                if (!solutionFound) {
                    solutionFound = true;

                    this.workers.forEach((w) => w.terminate());

                    const nonce = e.data;
                    this.currentSolutions.set(salt, nonce);

                    const hiddenInput = document.createElement("input");
                    hiddenInput.type = "hidden";
                    hiddenInput.name = "powbox_solution";
                    hiddenInput.value = nonce;
                    form.appendChild(hiddenInput);
                }
            };

            const batchSize = 1000;
            worker.postMessage({
                salt: salt,
                prefix,
                start: index,
                step: this.workerCount,
                batchSize,
            });

            return worker;
        });
    }

    getSolution(salt) {
        return this.currentSolutions.get(salt) || null;
    }
}

document.addEventListener("DOMContentLoaded", function() {
    function executeCallback(callbackName) {
        if (typeof window[callbackName] === 'function') {
            window[callbackName]();
        }
    }

    const isDarkReaderDetected = [...document.querySelectorAll('style')].some((el) => el.classList.contains('darkreader'));

    const powBoxes = document.getElementsByClassName('powbox');

    if (powBoxes.length > 0) {
        addStyles(cssStyle);

        let powBox = powBoxes[0];
        let dataTheme = powBox.getAttribute('data-theme');

        if (dataTheme == null) {
            if (isDarkReaderDetected) {
                dataTheme = 'dark';
            } else if (powBox.classList.contains('light')) {
                dataTheme = 'light';
            } else if (powBox.classList.contains('dark')) {
                dataTheme = 'dark';
            }
        }

        addStyles(dataTheme === 'light' ? cssColorStyleLight : dataTheme === 'dark' ? cssColorStyleDark : cssColorStyle);
    }

    const powClient = new ProofOfWorkClient()

    function sleep(ms) {
        return new Promise(resolve => setTimeout(resolve, ms));
    }    
    
    async function waitForSolution(salt) {
        if (powClient.getSolution(salt)) {
            await sleep(2000);
            return;
        }

        return new Promise((resolve) => {
            const checkSolution = () => {
                const solution = powClient.getSolution(salt);
                if (solution) {
                    resolve(solution);
                } else {
                    setTimeout(checkSolution, 500);
                }
            };
            checkSolution();
        });
    }

    for (var i = 0; i < powBoxes.length; i++) {
        let powBox = powBoxes[i];

        let dataLanguage = powBox.getAttribute('data-lang');

        if (dataLanguage == null) {
            dataLanguage = powBox.getAttribute('data-language');
            if (dataLanguage == null) {
                dataLanguage = getNearestLanguage();
            }
        }

        let boxHTML = htmlContent.replaceAll('FOOTER', translate(
            'Source code on <a href=\"https://github.com/tn3w/TrueClick\" target=\"_blank\">GitHub</a>', dataLanguage
        ));
        boxHTML = boxHTML.replaceAll('ROBOT', translate('I\'m not a robot.', dataLanguage));

        powBox.innerHTML = boxHTML;

        let dataChallenge = powBox.getAttribute("data-challenge");
        let dataDifficulty = powBox.getAttribute("data-difficulty");
        let dataCallback = powBox.getAttribute('data-callback');

        if (dataChallenge == null) {
            showError(powBox, "No challenge was given! Reload the page.")
            return;
        }

        let difficulty = Number(dataDifficulty);
        if (isNaN(difficulty) || dataDifficulty === undefined || dataDifficulty === null || dataDifficulty === '') {
            difficulty = 5;
        }

        let form = powBox.closest('form') || document.forms[0];

        powClient.startWorkers(
            dataChallenge,
            difficulty,
            form
        );

        let checkbox = powBox.querySelector('input[type="checkbox"]');
        if (checkbox) {
            checkbox.required = true;
        }        
        
        let svg = powBox.querySelector('svg');
        let paragraph = powBox.querySelector('p');

        function hideLoading() {
            checkbox.style.display = 'unset';
            svg.style.display = 'none';
            paragraph.innerText = translate('Success!', dataLanguage);
        }

        function showLoading() {
            checkbox.style.display = 'none';
            svg.style.display = 'block';
            paragraph.innerText = translate('Verifying ...', dataLanguage);
        }

        checkbox.addEventListener('change', async function() {            
            if (this.checked) {
                this.checked = false;
                showLoading();

                await waitForSolution(dataChallenge);

                if (dataCallback != null) {
                    executeCallback(dataCallback);
                }

                hideLoading();
            }
            this.checked = true;
        });
    }
});