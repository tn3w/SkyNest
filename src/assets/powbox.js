function getLanguageCode(code) {
    if (code.length === 2 && VALID_LANGUAGES.includes(code)) {
        return code;
    }

    if (code.length === 3 && LANGUAGES_3_TO_2[code]) {
        const mappedCode = LANGUAGES_3_TO_2[code];

        if (VALID_LANGUAGES.includes(mappedCode)) {
            return mappedCode;
        }
    }

    return 'en';
}  

function getNearestLanguage() {
    return getLanguageCode(navigator.language.split('-')[0]);
}

function addStyles(css) {
    var styleElement = Array.from(document.querySelectorAll("head style"))
        .find(el => !el.classList.contains('darkreader')) || document.createElement("style");

    if (styleElement.styleSheet) {
        styleElement.styleSheet.cssText += css;  // For IE
    } else {
        styleElement.appendChild(document.createTextNode(css));  // Standard
    }

    if (!styleElement.parentNode) {
        document.head.appendChild(styleElement);
    }

    return styleElement;
}

function removeStyles(css) {
    const styleSheets = document.querySelectorAll('style');

    styleSheets.forEach((styleElement) => {
        let cssText = '';
        if (styleElement.styleSheet) {
            cssText = styleElement.styleSheet.cssText;
        } else {
            cssText = styleElement.textContent;
        }

        if (cssText.includes(css)) {
            cssText = cssText.replace(css, '');
            if (styleElement.styleSheet) {
                styleElement.styleSheet.cssText = cssText;
            } else {
                styleElement.textContent = cssText;
            }

            if (cssText.trim() === '') {
                styleElement.remove();
            }
        }
    });
}

