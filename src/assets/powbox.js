function getLanguageCode(code) {
    if (code.length === 2 && VALID_LANGUAGES.includes(code)) {
        return code;
    }

    if (code.length === 3 && LANGUAGES_3_TO_2[code]) {
        const mappedCode = LANGUAGES_3_TO_2[code];

        if (VALID_LANGUAGES.includes(mappedCode)) {
            return mappedCode;
        }
    }

    return 'en';
}  

